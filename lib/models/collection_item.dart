class CollectionItem {
  final String id;
  final String title;
  final String desc;
  final String imagePath;
  final double price;
  final double minPrice;
  final double maxPrice;

  CollectionItem({
    required this.id,
    required this.title,
    required this.desc,
    required this.imagePath,
    required this.price,
    required this.minPrice,
    required this.maxPrice,
  });
}
